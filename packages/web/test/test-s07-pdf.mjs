// Build a fakePdf object compatible with parsePdf. `pageItems` is an array
// of getTextContent items; `annotations` and `attachments` map onto the
// matching pdf-doc accessors. Each helper returns a fresh structure to keep
// tests independent.
function makeFakePdf({ pageItems = [[]], annotations = [], attachments = {}, info = {}, fieldObjects = {} } = {}) {
  return {
    numPages: pageItems.length,
    async getPage(i) {
      const items = pageItems[i - 1] || [];
      const annots = annotations[i - 1] || [];
      return {
        async getTextContent() { return { items }; },
        async getAnnotations() { return annots; },
      };
    },
    async getAttachments() { return attachments; },
    async getMetadata() { return { info, metadata: null }; },
    async getFieldObjects() { return fieldObjects; },
  };
}

// Install a globalThis.pdfjsLib mock whose getDocument returns the supplied
// fakePdf. The caller is responsible for cleanup via the returned restore fn.
function installPdfjsMock(fakePdf) {
  const prev = globalThis.pdfjsLib;
  globalThis.pdfjsLib = {
    getDocument() { return { promise: Promise.resolve(fakePdf) }; },
  };
  return () => {
    if (prev === undefined) delete globalThis.pdfjsLib;
    else globalThis.pdfjsLib = prev;
  };
}

