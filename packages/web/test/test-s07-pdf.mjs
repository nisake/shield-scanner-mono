// --- Test 46: PDF-in-PDF respects _PDF_RECURSION_LIMIT (no infinite recursion) ---
add('46 S7 Stage B: PDF-in-PDF respects _PDF_RECURSION_LIMIT (no infinite recursion)', async () => {
  // Sanity-pin the depth bound (kept at module top-level constant).
  if (_PDF_RECURSION_LIMIT !== 2) throw new Error(`_PDF_RECURSION_LIMIT=${_PDF_RECURSION_LIMIT}, expected 2`);

  let innerCalls = 0;
  const selfPdfBytes = new Uint8Array([0x25, 0x50, 0x44, 0x46]);
  // Self-referential pdf: every getPage increments innerCalls; the 'self.pdf'
  // attachment points back at the same pdf bytes (mock always returns the
  // same fakePdf via getDocument).
  const fakePdf = {
    numPages: 1,
    async getPage(i) {
      innerCalls++;
      return {
        async getTextContent() {
          return { items: [{ str: 'inner body', height: 10 }] };
        },
        async getAnnotations() { return []; },
      };
    },
    async getAttachments() {
      return { 'self.pdf': { filename: 'self.pdf', content: selfPdfBytes } };
    },
    async getMetadata() { return { info: {}, metadata: null }; },
    async getFieldObjects() { return {}; },
  };
  const restore = installPdfjsMock(fakePdf);
  try {
    const out = await parsePdf(selfPdfBytes);
    // depth 0 outer -> page=1 (call 1); recurses into depth 1 -> page=1 (call 2);
    // depth 1 still < limit(2), recurses into depth 2 -> page=1 (call 3);
    // at depth 2 (=== limit) the recursion-into-attachments branch is skipped.
    if (innerCalls !== 3) {
      throw new Error(`innerCalls=${innerCalls}, expected 3 (depth 0/1/2 each call getPage once)`);
    }
    if (!out.text.includes('inner body')) {
      throw new Error(`body missing 'inner body'. text=${JSON.stringify(out.text).slice(0, 300)}`);
    }
  } finally { restore(); }
});

// --- Test 47: child contextLocation prefixed, not overwritten ---
add('47 S7 Stage B: child contextLocation prefixed, not overwritten', async () => {
  let invocation = 0;
  // Custom getDocument mock: returns outer pdf first, inner pdf next.
  const outerBytes = new Uint8Array([0x25, 0x50, 0x44, 0x46]);
  const childBytes = new Uint8Array([0x25, 0x50, 0x44, 0x46, 0x01]);
  const outerPdf = {
    numPages: 1,
    async getPage() {
      return {
        async getTextContent() { return { items: [] }; },
        async getAnnotations() { return []; },
      };
    },
    async getAttachments() {
      return { 'child.pdf': { filename: 'child.pdf', content: childBytes } };
    },
    async getMetadata() { return { info: {}, metadata: null }; },
    async getFieldObjects() { return {}; },
  };
  const innerPdf = {
    numPages: 1,
    async getPage() {
      return {
        async getTextContent() {
          // height < 1 triggers microscopic-text finding with contextLocation='Page 1'.
          return { items: [{ str: 'invisible payload', height: 0.5 }] };
        },
        async getAnnotations() { return []; },
      };
    },
    async getAttachments() { return {}; },
    async getMetadata() { return { info: {}, metadata: null }; },
    async getFieldObjects() { return {}; },
  };

  const prev = globalThis.pdfjsLib;
  globalThis.pdfjsLib = {
    getDocument() {
      invocation++;
      const target = invocation === 1 ? outerPdf : innerPdf;
      return { promise: Promise.resolve(target) };
    },
  };
  try {
    const out = await parsePdf(outerBytes);
    const microHit = (out.hiddenFindings || []).find((f) => f.technique === 'microscopic-text');
    if (!microHit) {
      throw new Error(`no microscopic-text finding. findings=${JSON.stringify(out.hiddenFindings)}`);
    }
    if (microHit.contextLocation !== 'Attachment child.pdf > Page 1') {
      throw new Error(`contextLocation mismatch: ${microHit.contextLocation}`);
    }
    // v1.12.0 Theme A: meta.height carries the numeric height (separated from
    // the kebab-case technique id).
    if (!microHit.meta || typeof microHit.meta.height !== 'number' || microHit.meta.height !== 0.5) {
      throw new Error(`meta.height mismatch: ${JSON.stringify(microHit.meta)}`);
    }
  } finally {
    if (prev === undefined) delete globalThis.pdfjsLib;
    else globalThis.pdfjsLib = prev;
  }
});

