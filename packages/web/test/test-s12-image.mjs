let passed = 0;
let failed = 0;
const failures = [];

for (const t of tests) {
  let ok = false;
  let err = null;
  try {
    ok = !!(await t.run());
  } catch (e) {
    err = e;
  }
  if (ok) {
    passed++;
    console.log(`PASS ${t.name}`);
  } else {
    failed++;
    console.log(`FAIL ${t.name}`);
    if (err) console.log('       error:', err && err.stack ? err.stack.split('\n').slice(0, 3).join('\n') : err);
    failures.push(t.name);
  }
}

// Sanity check: _imgDecodeUtf8OrLatin1 must be a function (BYPASS-01 surface)
if (typeof _imgDecodeUtf8OrLatin1 !== 'function') {
  failed++;
  console.log('FAIL sanity: _imgDecodeUtf8OrLatin1 is not exported as a function');
} else {
  passed++;
  console.log('PASS sanity: _imgDecodeUtf8OrLatin1 exported');
}

console.log(`\nTotal: ${passed} passed / ${failed} failed`);
process.exitCode = failed === 0 ? 0 : 1;
