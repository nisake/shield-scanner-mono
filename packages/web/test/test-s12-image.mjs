function crc32(buf) {
  let c;
  const table = crc32.table || (crc32.table = (() => {
    const t = new Uint32Array(256);
    for (let n = 0; n < 256; n++) {
      c = n;
      for (let k = 0; k < 8; k++) {
        c = (c & 1) ? (0xEDB88320 ^ (c >>> 1)) : (c >>> 1);
      }
      t[n] = c >>> 0;
    }
    return t;
  })());
  let crc = 0xFFFFFFFF;
  for (let i = 0; i < buf.length; i++) {
    crc = table[(crc ^ buf[i]) & 0xFF] ^ (crc >>> 8);
  }
  return (crc ^ 0xFFFFFFFF) >>> 0;
}

function pngChunk(type, data) {
  const len = Buffer.alloc(4);
  len.writeUInt32BE(data.length, 0);
  const typeBuf = Buffer.from(type, 'latin1');
  const crcInput = Buffer.concat([typeBuf, data]);
  const crc = Buffer.alloc(4);
  crc.writeUInt32BE(crc32(crcInput), 0);
  return Buffer.concat([len, typeBuf, data, crc]);
}

const PNG_SIG = Buffer.from([0x89, 0x50, 0x4E, 0x47, 0x0D, 0x0A, 0x1A, 0x0A]);
function pngIHDR() {
  // minimal 1x1 IHDR: width=1 height=1 bit-depth=8 color-type=2(RGB) compression=0 filter=0 interlace=0
  const d = Buffer.alloc(13);
  d.writeUInt32BE(1, 0); // width
  d.writeUInt32BE(1, 4); // height
  d[8] = 8;
  d[9] = 2;
  d[10] = 0;
  d[11] = 0;
  d[12] = 0;
  return pngChunk('IHDR', d);
}
function pngIDAT() {
  // 1x1 RGB filter-0 + 3 zero bytes, deflate-compressed (raw block)
  // Use a pre-built deflate stream of [0x00, 0x00, 0x00, 0x00]: filter byte + RGB
  const raw = Buffer.from([0x00, 0x00, 0x00, 0x00]);
  // deflate stored block: BFINAL=1 BTYPE=00, len=4, nlen=~4
  const stored = Buffer.concat([
    Buffer.from([0x78, 0x01]), // zlib header
    Buffer.from([0x01, 0x04, 0x00, 0xFB, 0xFF]), // stored block header (final)
    raw,
    // adler32 of raw
    (() => {
      let a = 1, b = 0;
      for (const x of raw) { a = (a + x) % 65521; b = (b + a) % 65521; }
      const out = Buffer.alloc(4);
      out.writeUInt32BE(((b << 16) | a) >>> 0, 0);
      return out;
    })(),
  ]);
  return pngChunk('IDAT', stored);
}
function pngIEND() { return pngChunk('IEND', Buffer.alloc(0)); }

// ---------------------------------------------------------------
//  Local JPEG synth helper (used by Tests 71, 73, 74)
// ---------------------------------------------------------------
function jpegWithXmpPacket(xmpPayload) {
  // SOI + APP1 (Exif placeholder header replaced with XMP) + EOI
  const soi = Buffer.from([0xFF, 0xD8]);
  const eoi = Buffer.from([0xFF, 0xD9]);
  const xmpHeader = Buffer.concat([
    Buffer.from('http://ns.adobe.com/xap/1.0/', 'latin1'),
    Buffer.from([0x00]),
  ]);
  const xmp = Buffer.from(xmpPayload, 'utf8');
  const segContent = Buffer.concat([xmpHeader, xmp]);
  const segLen = Buffer.alloc(2);
  segLen.writeUInt16BE(segContent.length + 2, 0); // length field includes itself
  const app1 = Buffer.concat([Buffer.from([0xFF, 0xE1]), segLen, segContent]);
  return Buffer.concat([soi, app1, eoi]);
}

